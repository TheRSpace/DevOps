package com.test.DevOpsTest;

import java.util.Date;

public class Trainer {
	private String name;
	private int lvl = 0;
	private int xp = 0;
	
	public Trainer(String name) {
		setName(name);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getLvl() {
		return lvl;
	}

	public void setLvl(int lvl) {
		this.lvl = lvl;
	}

	public int getXp() {
		return xp;
	}

	public void setXp(int xp) {
		this.xp = xp;
	}
	
}
